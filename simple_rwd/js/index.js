$(document).ready(function(){
    $('.bxslider').bxSlider({
        pager: false
    });

    $('.main-nav__list-item').on('click', 'a', function (e) {
        e.preventDefault();

        var target = this.hash;
        var $target = $(target);

        $('html, body').stop().animate({
            'scrollTop': $target.offset().top
        }, 900, 'swing', function () {
            window.location.hash = target;
        });
    });

});

